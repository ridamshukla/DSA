package String;

public class BasicString {
    public static void main(String[] args){
        // Direct Initialization

        String s = "Ridam Shukla";

        // Concataion

        String r = "Brahman";
        String rs = s+" "+r;

        System.out.println(rs);

        // Char At to get Index


        System.out.println(s.charAt(3));
    }
}
